    public function berhasil(Request $request)
    {
      $email = $request->email;
      $password = $request->password;
      if(Auth::attempt(['email'=>$email, 'password'=>$password])){
        return redirect('speedshop');
      }
      else{
        return redirect('masuk')
          ->with('errorMessage','maaf login gagal');
      }

    }

